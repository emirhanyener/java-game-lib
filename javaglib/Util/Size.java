package Util;

public class Size {
	private float width;
	private float height;
	
	public Size(float width, float height) {
		this.width = width;
		this.height = height;
	}
	
	public float getHeight() {
		return height;
	}
	public float getWidth() {
		return width;
	}
	
	public void setHeight(float height) {
		this.height = height;
	}
	public void setWidth(float width) {
		this.width = width;
	}
}
